import Foundation
import UIKit

extension UIColor {
    @nonobjc class var mocoBlue: UIColor {
        return UIColor(red: 37.0 / 255.0, green: 206.0 / 255.0, blue: 209.0 / 255.0, alpha: 1.0)
      }

      @nonobjc class var mocoPink: UIColor {
        return UIColor(red: 234.0 / 255.0, green: 82.0 / 255.0, blue: 111.0 / 255.0, alpha: 1.0)
      }

      @nonobjc class var mocoOrange: UIColor {
        return UIColor(red: 1.0, green: 138.0 / 255.0, blue: 91.0 / 255.0, alpha: 1.0)
      }

      @nonobjc class var mocoGreen: UIColor {
        return UIColor(red: 6.0 / 255.0, green: 214.0 / 255.0, blue: 160.0 / 255.0, alpha: 1.0)
      }

      @nonobjc class var mocoRed: UIColor {
        return UIColor(red: 235.0 / 255.0, green: 87.0 / 255.0, blue: 87.0 / 255.0, alpha: 1.0)
      }
    
}
